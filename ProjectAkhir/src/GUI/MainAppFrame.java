    private void loadUsersToComboBox() {
        DefaultComboBoxModel<User> model = new DefaultComboBoxModel<>();
        if (allUsers.isEmpty()) {
            btnLanjutkan.setEnabled(false);
            btnHapusUser.setEnabled(false);
        } else {
            btnLanjutkan.setEnabled(true);
            btnHapusUser.setEnabled(true);
            for (User user : allUsers) {
                model.addElement(user);
            }
        }
        cmbPilihUser.setModel(model);
        if (allUsers.isEmpty()) {
            cmbPilihUser.setSelectedItem(null);
        }
    }
    

