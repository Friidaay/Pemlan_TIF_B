    private void btnHapusUserActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnHapusUserActionPerformed
        User userToDelete = (User) cmbPilihUser.getSelectedItem();
        if (userToDelete == null) {
            JOptionPane.showMessageDialog(this, "Pilih pengguna yang ingin dihapus!", "Peringatan", JOptionPane.WARNING_MESSAGE);
            return;
        }

        int confirm = JOptionPane.showConfirmDialog(this,
            "Apakah Anda yakin ingin menghapus pengguna '" + userToDelete.getNama() + "' dan semua datanya?",
            "Konfirmasi Hapus", JOptionPane.YES_NO_OPTION, JOptionPane.WARNING_MESSAGE);

        if (confirm == JOptionPane.YES_OPTION) {
            allUsers.remove(userToDelete);
            FileStorageUtil.saveAllUsers(allUsers);
            JOptionPane.showMessageDialog(this, "Pengguna '" + userToDelete.getNama() + "' berhasil dihapus.", "Sukses", JOptionPane.INFORMATION_MESSAGE);
            loadUsersToComboBox();
        }
    }//GEN-LAST:event_btnHapusUserActionPerformed

    private void btnLanjutkanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLanjutkanActionPerformed
        currentUser = (User) cmbPilihUser.getSelectedItem();
        if (currentUser == null) {
            JOptionPane.showMessageDialog(this, "Pilih pengguna terlebih dahulu!", "Peringatan", JOptionPane.WARNING_MESSAGE);
            return;
        }

        pnlLogin.setVisible(false);

        if (dailyPanel == null) {
            dailyPanel = new DailyCaloriePanel(currentUser, allUsers, this); 
            tabbedPaneMain.addTab("Input & Hitung Kalori", dailyPanel);
        } else {
            dailyPanel.setCurrentUser(currentUser);
            dailyPanel.loadOrCreateDailyEntry();
        }

        if (historyPanel == null) {
            historyPanel = new HistoryPanel(currentUser);
            tabbedPaneMain.addTab("Riwayat Harian", historyPanel);
        } else {
            historyPanel.setCurrentUser(currentUser);
            historyPanel.loadHistoryData();
        }
        
        tabbedPaneMain.setVisible(true);
        tabbedPaneMain.setSelectedIndex(0);

        tabbedPaneMain.addChangeListener(new ChangeListener() {
            @Override
            public void stateChanged(ChangeEvent e) {
                if (tabbedPaneMain.getSelectedComponent() == historyPanel) {
                    historyPanel.loadHistoryData();
                }
            }
        });
    }//GEN-LAST:event_btnLanjutkanActionPerformed

    private void btnDaftarUserActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDaftarUserActionPerformed
        String nama = txtNamaUser.getText().trim();
        if (nama.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Nama pengguna tidak boleh kosong!", "Peringatan", JOptionPane.WARNING_MESSAGE);
            return;
        }

        boolean namaExists = allUsers.stream().anyMatch(u -> u.getNama().equalsIgnoreCase(nama));
        if (namaExists) {
            JOptionPane.showMessageDialog(this, "Nama pengguna sudah ada. Silakan gunakan nama lain.", "Error Duplikasi", JOptionPane.ERROR_MESSAGE);
            return;
        }

        User newUser = new User(nama);
        allUsers.add(newUser);
        FileStorageUtil.saveAllUsers(allUsers);

        JOptionPane.showMessageDialog(this, "Pengguna '" + nama + "' berhasil didaftarkan", "Sukses", JOptionPane.INFORMATION_MESSAGE);
        txtNamaUser.setText("");
        loadUsersToComboBox();
    }//GEN-LAST:event_btnDaftarUserActionPerformed

